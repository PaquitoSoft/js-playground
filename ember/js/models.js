// Models
App.Store = DS.Store.extend({
	revision: 11,
	adapter: 'DS.FixtureAdapter'
});

App.Brand = DS.Model.extend({
	code: DS.attr('string'),
	products: DS.hasMany('App.Product')
});

App.Product = DS.Model.extend({
	name: DS.attr('string'),
	imgUrl: DS.attr('string'),
	price: DS.attr('number'),
	brand: DS.belongsTo('App.Brand')
});

App.ShopCart = DS.Model.extend({
	orderItems: DS.hasMany('App.OrderItem'),
	totalPrice: function() {
		return this.get('orderItems').getEach('totalPrice').reduce(function(accum, item) {
			return accum + item;
		}, 0);
	}.property('orderItems.@each.totalPrice')
});

App.OrderItem = DS.Model.extend({
	quantity: DS.attr('number'),
	product: DS.belongsTo('App.Product'),
	totalPrice: function() {
		return this.get('product').price * this.get('quantity');
	}.property('product', 'quantity')
});

